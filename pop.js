const friends = ["Balam", "Habib", "Hridoy"];

//Print Basic Array:
console.log("BasiC Array: ",friends);

//Push Element:
friends.push("Arefin Rumy");

//Print Array After Push:
console.log("After Push One Element: ",friends);

//Pop (remove) The element from Array and keep it in a const:
const out1 = friends.pop();

//Print Array After Pop:
console.log("After Pop One Element: ",friends);

//The element which we pop:
console.log("The element we remove: ", out1);

//The Next Element We Pop:
const out2 = friends.pop();

//After Pop The Array:
console.log("After Pop Another Element: ", friends);

//The Element We Pop:
console.log("The Element We Pop: ", out2);


 